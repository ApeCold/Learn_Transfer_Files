package cn.bsd.learn.transfer.files.bean;

public class FileInfo {

    // 存放路径
    private String path;
    // 文件大小
    private String size;
    // 文件名称
    private String name;

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
